import math

def cuadrado(L):
    area = pow(L, 2)
    return print("El area del cuadrado es: ", area)

def circulo(R):
    area=math.pi*R**2
    return print("El area del circulo es: ", area)

def triangulo(a, b):
    area = a*b/2
    return print("El area del triangulo es: ", area)


